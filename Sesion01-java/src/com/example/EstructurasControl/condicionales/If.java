package com.example.EstructurasControl.condicionales;

public class If {

    public static void main(String[] args) {

        int edad = 17;
        System.out.println(edad);

        if (edad >= 18) {
            System.out.println("Es mayor de edad");
        } else {
            System.out.println("Es menor de edad");
        }
    }
}
